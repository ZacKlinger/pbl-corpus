#!/usr/bin/env bash
# Regenerate INDEX.md from the current state of the resource bank.
# Run at the end of each Saturday session, before the final commit + push.
#
# Output: INDEX.md at repo root, listing every source, claim-evidence thread,
# leads file, digest, and session log with counts and last-modified dates.
# Reads frontmatter for sources/ entries (title, year, layer, read_status).

set -euo pipefail

cd "$(dirname "$0")/.."

OUT="INDEX.md"

get_frontmatter_field() {
    # $1 = file, $2 = field name. Prints the value or empty string.
    awk -v field="$2" '
        /^---$/ { if (++seen == 2) exit; next }
        seen == 1 && $0 ~ "^"field":" {
            sub("^"field":[ \t]*", "")
            print
            exit
        }
    ' "$1"
}

last_mod() {
    # ISO date for last commit touching this path, or filesystem mtime if untracked.
    local d
    d=$(git log -1 --format=%cs -- "$1" 2>/dev/null || true)
    if [ -z "$d" ]; then
        d=$(date -r "$1" +%Y-%m-%d 2>/dev/null || echo "—")
    fi
    echo "$d"
}

count_md() {
    # Count .md files in a directory, excluding README.md
    find "$1" -maxdepth 1 -type f -name '*.md' ! -name 'README.md' 2>/dev/null | wc -l | tr -d ' '
}

n_sources=$(count_md sources)
n_claims=$(count_md claim-evidence)
n_leads=$(count_md leads)
n_digests=$(count_md digests)
n_sessions=$(count_md sessions)
n_raw=$(find sources-raw -maxdepth 1 -type f ! -name 'README.md' 2>/dev/null | wc -l | tr -d ' ')

{
    echo "# Resource bank index"
    echo
    echo "Auto-generated by \`bin/build-index.sh\`. Do not edit by hand — regenerated at the end of each session. To browse a section, follow a link."
    echo
    echo "_Last regenerated: $(date -u +%Y-%m-%d)_"
    echo
    echo "## At a glance"
    echo
    echo "| Section | Count |"
    echo "|---|---|"
    echo "| Verified sources (read & quoted) | $n_sources |"
    echo "| Claim-evidence threads | $n_claims |"
    echo "| Leads files (unverified material) | $n_leads |"
    echo "| Raw primary-source files awaiting processing | $n_raw |"
    echo "| Weekly digests | $n_digests |"
    echo "| Session logs | $n_sessions |"
    echo

    # --- Sources ---
    echo "## Verified sources"
    echo
    echo "Each entry: short title — layer — read status — last updated. Click through for the full source note (frontmatter, verbatim quotes, tensions)."
    echo
    if [ "$n_sources" -gt 0 ]; then
        for f in sources/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            title=$(get_frontmatter_field "$f" title)
            layer=$(get_frontmatter_field "$f" layer)
            status=$(get_frontmatter_field "$f" read_status)
            date=$(last_mod "$f")
            base=$(basename "$f" .md)
            echo "- [$base]($f) — ${layer:-?} — ${status:-?} — $date"
            [ -n "$title" ] && echo "  - $title"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Claim-evidence threads ---
    echo "## Claim-evidence threads"
    echo
    echo "Each thread tracks supporting and counter-evidence for one thesis seed. Claim count is grep'd from \`## Claim\` headings; the count is approximate while threads are still in their first-claim phase."
    echo
    if [ "$n_claims" -gt 0 ]; then
        for f in claim-evidence/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            claims=$(grep -c '^## Claim' "$f" 2>/dev/null || echo 0)
            date=$(last_mod "$f")
            base=$(basename "$f" .md)
            echo "- [$base]($f) — ${claims} claim(s) — last updated $date"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Leads ---
    echo "## Leads (unverified material)"
    echo
    echo "Material the agent has heard about but not yet read. Promotion to claim-evidence requires a verbatim quote, URL, and access date."
    echo
    if [ "$n_leads" -gt 0 ]; then
        for f in leads/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            date=$(last_mod "$f")
            base=$(basename "$f" .md)
            echo "- [$base]($f) — last updated $date"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Raw primary sources awaiting processing ---
    echo "## Raw primary-source files awaiting processing"
    echo
    echo "Files in \`sources-raw/\` that the fetcher workflow has delivered but the agent has not yet produced a verified source note for. The agent reads from here each Saturday."
    echo
    if [ "$n_raw" -gt 0 ]; then
        for f in sources-raw/*; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            base=$(basename "$f")
            stem="${base%.*}"
            if [ -f "sources/${stem}.md" ]; then
                continue
            fi
            size=$(wc -c <"$f" | tr -d ' ')
            echo "- \`$base\` ($(( size / 1024 ))KB)"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Digests ---
    echo "## Weekly digests"
    echo
    echo "Newest first. Each digest is the artifact written for Zack — start here when catching up."
    echo
    if [ "$n_digests" -gt 0 ]; then
        for f in $(ls -1 digests/*.md 2>/dev/null | sort -r); do
            [ "$(basename "$f")" = "README.md" ] && continue
            base=$(basename "$f" .md)
            echo "- [$base]($f)"
        done
    else
        echo "_(empty)_"
    fi
} > "$OUT"

echo "wrote $OUT"

# Also regenerate the research-web graph if the extractor is present.
if [ -x bin/build-graph.py ]; then
    python3 bin/build-graph.py
fi
