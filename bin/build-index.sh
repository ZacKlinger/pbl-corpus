#!/usr/bin/env bash
# Regenerate INDEX.md from the current state of the resource bank.
# Run at the end of each Saturday session, before the final commit + push.
#
# Output: INDEX.md at repo root. Sources grouped by layer with year + read
# status + a one-line thesis pulled from each source note's `## Thesis`
# section + back-references to claim-evidence threads that cite the source.
# Leads grouped by status. Raw and digests unchanged.

set -euo pipefail

cd "$(dirname "$0")/.."

OUT="INDEX.md"

get_frontmatter_field() {
    # $1 = file, $2 = field name. Prints the value or empty string.
    awk -v field="$2" '
        /^---$/ { if (++seen == 2) exit; next }
        seen == 1 && $0 ~ "^"field":" {
            sub("^"field":[ \t]*", "")
            print
            exit
        }
    ' "$1"
}

get_thesis_oneline() {
    # Pull the first ~200 chars of the `## Thesis` section, collapsed to one line.
    awk '
        /^## Thesis[ \t]*$/ { capturing = 1; next }
        capturing && /^## / { exit }
        capturing { print }
    ' "$1" | tr '\n' ' ' | sed 's/  */ /g; s/^ *//; s/ *$//' | cut -c1-220 | awk '{ if (length($0) >= 220) sub(/ [^ ]*$/, ""); print $0 "â€¦" }'
}

last_mod() {
    local d
    d=$(git log -1 --format=%cs -- "$1" 2>/dev/null || true)
    if [ -z "$d" ]; then
        d=$(date -r "$1" +%Y-%m-%d 2>/dev/null || echo "â€”")
    fi
    echo "$d"
}

count_md() {
    find "$1" -maxdepth 1 -type f -name '*.md' ! -name 'README.md' 2>/dev/null | wc -l | tr -d ' '
}

# Threads (claim-evidence files) that cite a given source by relative-path link.
threads_citing() {
    local src_path="$1"   # e.g. sources/Foo-2017.md
    local stem
    stem=$(basename "$src_path" .md)
    # Look for ](../sources/<stem>.md) anywhere in claim-evidence files
    grep -l "](../sources/${stem}.md)" claim-evidence/*.md 2>/dev/null | while read -r f; do
        basename "$f" .md
    done | sort -u
}

n_sources=$(count_md sources)
n_claims=$(count_md claim-evidence)
n_leads=$(count_md leads)
n_digests=$(count_md digests)
n_sessions=$(count_md sessions)
n_raw=$(find sources-raw -maxdepth 1 -type f ! -name 'README.md' 2>/dev/null | wc -l | tr -d ' ')

{
    echo "# Resource bank index"
    echo
    echo "Auto-generated by \`bin/build-index.sh\`. Do not edit by hand â€” regenerated at the end of each session. The interactive [research web](https://zacklinger.github.io/pbl-corpus/) renders the same data as a graph and a catalog."
    echo
    echo "_Last regenerated: $(date -u +%Y-%m-%d)_"
    echo
    echo "## At a glance"
    echo
    echo "| Section | Count |"
    echo "|---|---|"
    echo "| Verified sources (read & quoted) | $n_sources |"
    echo "| Claim-evidence threads | $n_claims |"
    echo "| Leads files (unverified material) | $n_leads |"
    echo "| Raw primary-source files awaiting processing | $n_raw |"
    echo "| Weekly digests | $n_digests |"
    echo "| Session logs | $n_sessions |"
    echo

    # --- Sources grouped by layer ----------------------------------------
    echo "## Verified sources"
    echo
    if [ "$n_sources" -eq 0 ]; then
        echo "_(empty)_"
        echo
    else
        for layer in empirical visionary practitioner; do
            # Collect matching files for this layer
            matching=()
            for f in sources/*.md; do
                [ -f "$f" ] || continue
                [ "$(basename "$f")" = "README.md" ] && continue
                l=$(get_frontmatter_field "$f" layer)
                if [ "$l" = "$layer" ]; then
                    matching+=("$f")
                fi
            done
            if [ "${#matching[@]}" -eq 0 ]; then
                continue
            fi
            # Capitalize the layer name (zsh and bash both support ^)
            layer_label="$(echo "$layer" | tr '[:lower:]' '[:upper:]' | cut -c1)$(echo "$layer" | cut -c2-)"
            echo "### ${layer_label} (${#matching[@]})"
            echo
            for f in "${matching[@]}"; do
                title=$(get_frontmatter_field "$f" title)
                year=$(get_frontmatter_field "$f" year)
                status=$(get_frontmatter_field "$f" read_status)
                url=$(get_frontmatter_field "$f" url)
                access=$(get_frontmatter_field "$f" access_date)
                base=$(basename "$f" .md)
                thesis=$(get_thesis_oneline "$f")

                # Back-refs: which claim-evidence threads cite this source
                threads_list=$(threads_citing "$f" | paste -sd "," -)

                echo "- **[$base]($f)** â€” ${year:-?} Â· ${status:-?}${access:+ Â· accessed $access}"
                [ -n "$title" ] && echo "  - *${title}*"
                [ -n "$thesis" ] && echo "  - ${thesis}"
                if [ -n "$threads_list" ]; then
                    # Linkify each thread, then format with comma-space joiners
                    linked=$(echo "$threads_list" | sed -E 's#([a-zA-Z0-9_-]+)#[\1](claim-evidence/\1.md)#g; s#,#, #g')
                    echo "  - Cited in: $linked"
                fi
                if [ -n "$url" ]; then
                    echo "  - [Original â†—]($url)"
                fi
                # Raw file link if present
                raw_match=$(find sources-raw -maxdepth 1 -type f -name "${base}.*" 2>/dev/null | head -1)
                if [ -n "$raw_match" ]; then
                    echo "  - [Raw file]($raw_match)"
                fi
            done
            echo
        done

        # Any sources whose layer doesn't match the three canonical values
        other_files=()
        for f in sources/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            l=$(get_frontmatter_field "$f" layer)
            case "$l" in
                empirical|visionary|practitioner) ;;
                *) other_files+=("$f") ;;
            esac
        done
        if [ "${#other_files[@]}" -gt 0 ]; then
            echo "### Other / unclassified (${#other_files[@]})"
            echo
            for f in "${other_files[@]}"; do
                base=$(basename "$f" .md)
                title=$(get_frontmatter_field "$f" title)
                year=$(get_frontmatter_field "$f" year)
                layer=$(get_frontmatter_field "$f" layer)
                echo "- [$base]($f) â€” ${layer:-?} â€” ${year:-?}"
                [ -n "$title" ] && echo "  - *${title}*"
            done
            echo
        fi
    fi

    # --- Claim-evidence threads ------------------------------------------
    echo "## Claim-evidence threads"
    echo
    echo "Each thread tracks supporting and counter-evidence for one strand of expertise. Claim count is grep'd from \`## Claim\` headings."
    echo
    if [ "$n_claims" -gt 0 ]; then
        for f in claim-evidence/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            claims=$(grep -c '^## Claim' "$f" 2>/dev/null || echo 0)
            date=$(last_mod "$f")
            base=$(basename "$f" .md)
            echo "- [**$base**]($f) â€” ${claims} claim(s) â€” last updated $date"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Leads -----------------------------------------------------------
    echo "## Leads (unverified material)"
    echo
    echo "Material the agent has heard about but not yet read. Promotion to a verified source requires reading the primary text and extracting verbatim quotes with locators."
    echo
    if [ "$n_leads" -gt 0 ]; then
        for f in leads/*.md; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            date=$(last_mod "$f")
            base=$(basename "$f" .md)
            # If the lead file has a top-line description, surface it
            tagline=$(awk 'NR>1 && /^[A-Z]/ {print; exit}' "$f" 2>/dev/null | cut -c1-160)
            echo "- [$base]($f) â€” last updated $date"
            [ -n "$tagline" ] && echo "  - ${tagline}"
        done
    else
        echo "_(empty)_"
    fi
    echo

    # --- Raw primary sources awaiting processing -------------------------
    echo "## Raw primary-source files awaiting processing"
    echo
    echo "Files in \`sources-raw/\` that the fetcher workflow has delivered but the agent has not yet produced a verified source note for."
    echo
    if [ "$n_raw" -gt 0 ]; then
        any_pending=0
        for f in sources-raw/*; do
            [ -f "$f" ] || continue
            [ "$(basename "$f")" = "README.md" ] && continue
            base=$(basename "$f")
            stem="${base%.*}"
            if [ -f "sources/${stem}.md" ]; then
                continue
            fi
            size=$(wc -c <"$f" | tr -d ' ')
            echo "- \`$base\` ($(( size / 1024 ))KB)"
            any_pending=1
        done
        if [ "$any_pending" -eq 0 ]; then
            echo "_(all raw files have been processed into verified source notes)_"
        fi
    else
        echo "_(empty)_"
    fi
    echo

    # --- Digests ---------------------------------------------------------
    echo "## Weekly digests"
    echo
    echo "Newest first. Each digest is the artifact written for Zack â€” start here when catching up."
    echo
    if [ "$n_digests" -gt 0 ]; then
        for f in $(ls -1 digests/*.md 2>/dev/null | sort -r); do
            [ "$(basename "$f")" = "README.md" ] && continue
            base=$(basename "$f" .md)
            echo "- [$base]($f)"
        done
    else
        echo "_(empty)_"
    fi
} > "$OUT"

echo "wrote $OUT"

# Also regenerate the research-web graph if the extractor is present.
if [ -x bin/build-graph.py ]; then
    python3 bin/build-graph.py
fi
